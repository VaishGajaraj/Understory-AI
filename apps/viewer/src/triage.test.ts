describe('centroid', () => {
  test('reproduces shapely for every geometry the label schema allows', () => {
    for (const fixture of SHAPELY_CENTROIDS) {
      const [lon, lat] = centroid(fixture.geometry)
      const delta = Math.max(
        Math.abs(lon - fixture.centroid[0]),
        Math.abs(lat - fixture.centroid[1]),
      )
      // Reported as an object so a failure names the geometry that drifted.
      expect({ case: fixture.name, matchesShapely: delta <= 1e-9 }).toEqual({
        case: fixture.name,
        matchesShapely: true,
      })
    }
  })

  test('area-weighted centroid of a square is its middle', () => {
    const [lon, lat] = centroid(square(-55.01, -7.02))
    expect(lon).toBeCloseTo(-55.01, 6)
    expect(lat).toBeCloseTo(-7.02, 6)
  })

  test('point geometry is its own centroid', () => {
    expect(centroid({ type: 'Point', coordinates: [-54.9, -6.9] })).toEqual([-54.9, -6.9])
  })

  test('a hole pulls the centroid away from the hole, not to the vertex mean', () => {
    // Vertex mean of shell + hole would be (2.5, 2.5); shapely says 5.2472….
    const [lon, lat] = centroid({
      type: 'Polygon',
      coordinates: [
        [
          [0, 0],
          [10, 0],
          [10, 10],
          [0, 10],
          [0, 0],
        ],
        [
          [1, 1],
          [4, 1],
          [4, 4],
          [1, 4],
          [1, 1],
        ],
      ],
    })
    expect(lon).toBeCloseTo(5.247252747252747, 12)
    expect(lat).toBeCloseTo(5.247252747252747, 12)
  })

  test('a line is length-weighted, not vertex-averaged', () => {
    // A schema-valid access-road label: one long segment, two short ones. The
    // vertex mean would be (8, 1.25); shapely walks the length.
    const [lon, lat] = centroid({
      type: 'LineString',
      coordinates: [
        [0, 0],
        [10, 0],
        [11, 0],
        [11, 5],
      ],
    })
    expect(lon).toBeCloseTo(7.21875, 12)
    expect(lat).toBeCloseTo(0.78125, 12)
  })

  test('a collapsed polygon answers with its boundary, like GEOS', () => {
    const [lon, lat] = centroid({
      type: 'Polygon',
      coordinates: [
        [
          [0, 0],
          [10, 0],
          [1, 0],
          [0, 0],
        ],
      ],
    })
    expect(lon).toBeCloseTo(5, 12)
    expect(lat).toBeCloseTo(0, 12)
  })

  test('degenerate ring falls back to the vertex mean', () => {
    const [lon, lat] = centroid({
      type: 'Polygon',
      coordinates: [
        [
          [-55, -7],
          [-55, -7],
          [-55, -7],
        ],
      ],
    })
    expect(lon).toBeCloseTo(-55, 9)
    expect(lat).toBeCloseTo(-7, 9)
  })

  test('an empty geometry has no centroid and so matches nothing', () => {
    // Shapely answers POINT EMPTY and the Python matcher raises reading .x off
    // it. NaN makes every distance comparison false — no match at 0°,0°.
    const [lon, lat] = centroid({ type: 'Polygon', coordinates: [] })
    expect(Number.isNaN(lon)).toBe(true)
    expect(Number.isNaN(lat)).toBe(true)
    expect(distanceM([lon, lat], [-55, -7]) <= 500).toBe(false)
  })
})

describe('distanceM', () => {
  test('matches the scoring harness approximation', () => {
    // 0.001 degrees of latitude = 110.54 m in scoring._distance_m.
    expect(distanceM([-55, -7], [-55, -7.001])).toBeCloseTo(110.54, 2)
  })
})

describe('temporal compatibility', () => {
  const event = label({ id: 'l1' })

  test('inside the padded window', () => {
    expect(temporallyCompatible(detection({ id: 'd', firstSeen: '2026-03-01' }), event, 36)).toBe(
      true,
    )
  })

  test('outside the padded window', () => {
    expect(temporallyCompatible(detection({ id: 'd', firstSeen: '2026-05-01' }), event, 36)).toBe(
      false,
    )
  })
})

describe('matchDetections', () => {
  test('greedy one-to-one, highest score first', () => {
    const near = detection({ id: 'near', score: 0.9, geometry: square(-55.0005, -7) })
    const far = detection({ id: 'far', score: 0.8, geometry: square(-55.001, -7) })
    const matches = matchDetections([far, near], [label({ id: 'l1' })], TOLERANCES)
    expect(matches.get('near')?.label.id).toBe('l1')
    expect(matches.has('far')).toBe(false)
  })

  test('nothing matches beyond the distance tolerance', () => {
    const matches = matchDetections(
      [detection({ id: 'd', geometry: square(-55.02, -7) })],
      [label({ id: 'l1' })],
      TOLERANCES,
    )
    expect(matches.size).toBe(0)
  })

  test('confirmed labels are matched before rejected ones', () => {
    // Both labels sit within tolerance; the confirmed pass runs first, so the
    // single detection lands on the confirmed label exactly as the harness does.
    const matches = matchDetections(
      [detection({ id: 'd', geometry: square(-55.001, -7) })],
      [
        label({ id: 'rejected-label', status: 'rejected', geometry: square(-55.0005, -7) }),
        label({ id: 'confirmed-label', status: 'confirmed', geometry: square(-55.001, -7) }),
      ],
      TOLERANCES,
    )
    expect(matches.get('d')?.label.id).toBe('confirmed-label')
  })

  test('a leftover detection can still land on a verified false alarm', () => {
    const matches = matchDetections(
      [
        detection({ id: 'strong', score: 0.9, geometry: square(-55, -7) }),
        detection({ id: 'weak', score: 0.4, geometry: square(-55.001, -7) }),
      ],
      [
        label({ id: 'confirmed-label', geometry: square(-55, -7) }),
        label({ id: 'rain', status: 'rejected', geometry: square(-55.001, -7) }),
      ],
      TOLERANCES,
    )
    expect(matches.get('strong')?.label.id).toBe('confirmed-label')
    expect(matches.get('weak')?.label.id).toBe('rain')
  })

  test('reports centroid separation, latency and optical lead', () => {
    const matches = matchDetections(
      [detection({ id: 'd', firstSeen: '2026-02-23T00:00:00', geometry: square(-55, -7.001) })],
      [label({ id: 'l1', opticalAlertDate: '2026-03-20' })],
      TOLERANCES,
    )
    const match = matches.get('d')
    expect(match?.distanceM).toBeCloseTo(110.54, 2)
    expect(match?.latencyDays).toBe(22)
    expect(match?.leadOverOpticalDays).toBe(25)
  })

  test('no optical alert date means no lead, not a zero', () => {
    const matches = matchDetections([detection({ id: 'd' })], [label({ id: 'l1' })], TOLERANCES)
    expect(matches.get('d')?.leadOverOpticalDays).toBeNull()
  })
})

describe('status resolution', () => {
  test('a detection inherits the status of the label it matched', () => {
    const rows = buildRows(
      [
        detection({ id: 'hit', score: 0.9, geometry: square(-55, -7) }),
        detection({ id: 'alarm', score: 0.6, geometry: square(-55.02, -7) }),
        detection({ id: 'orphan', score: 0.3, geometry: square(-55.5, -7) }),
      ],
      [
        label({ id: 'confirmed-label', geometry: square(-55, -7) }),
        label({ id: 'rain', status: 'rejected', geometry: square(-55.02, -7) }),
      ],
      TOLERANCES,
    )
    expect(rows.map((row) => row.status)).toEqual(['confirmed', 'rejected', 'candidate'])
    expect(rows.every((row) => !row.overridden)).toBe(true)
  })

  test('a local triage decision wins over the label-derived status', () => {
    const rows = buildRows(
      [detection({ id: 'hit', geometry: square(-55, -7) })],
      [label({ id: 'confirmed-label' })],
      TOLERANCES,
      { hit: 'rejected' },
    )
    expect(rows[0]?.status).toBe('rejected')
    expect(rows[0]?.overridden).toBe(true)
  })

  test('an unmatched detection is a candidate, never a confirmation', () => {
    expect(statusFromMatch(null)).toBe('candidate')
  })

  test('a candidate label does not confirm a detection', () => {
    const rows = buildRows(
      [detection({ id: 'd' })],
      [label({ id: 'unreviewed', status: 'candidate' })],
      TOLERANCES,
    )
    expect(rows[0]?.status).toBe('candidate')
    expect(rows[0]?.match?.label.id).toBe('unreviewed')
  })
})

describe('queue', () => {
  const rows = buildRows(
    [
      detection({
        id: 'a',
        score: 0.9,
        firstSeen: '2026-01-10T00:00:00',
        geometry: square(-55, -7),
      }),
      detection({
        id: 'b',
        score: 0.4,
        firstSeen: '2026-03-02T00:00:00',
        geometry: square(-56, -8),
      }),
      detection({
        id: 'c',
        score: 0.7,
        firstSeen: '2026-02-14T00:00:00',
        geometry: square(-57, -9),
      }),
    ],
    [label({ id: 'confirmed-label', geometry: square(-55, -7) })],
    TOLERANCES,
  )

  test('counts every filter bucket', () => {
    expect(filterCounts(rows)).toEqual({ all: 3, candidate: 2, confirmed: 1, rejected: 0 })
  })

  test('filters by resolved status', () => {
    expect(applyFilter(rows, 'confirmed').map((row) => row.detection.id)).toEqual(['a'])
    expect(applyFilter(rows, 'all')).toHaveLength(3)
  })

  test('sorts by score, highest first', () => {
    expect(sortQueue(rows, 'score').map((row) => row.detection.id)).toEqual(['a', 'c', 'b'])
  })

  test('sorts by recency, newest first', () => {
    expect(sortQueue(rows, 'recent').map((row) => row.detection.id)).toEqual(['b', 'c', 'a'])
  })

  test('queueFor filters then sorts', () => {
    expect(queueFor(rows, 'candidate', 'score').map((row) => row.detection.id)).toEqual(['c', 'b'])
  })
})

describe('windows and dates', () => {
  test('detection window spans earliest first_seen to latest last_seen', () => {
    expect(
      detectionWindow([
        detection({ id: 'a', firstSeen: '2026-02-13T00:00:00', lastSeen: '2026-03-01T00:00:00' }),
        detection({ id: 'b', firstSeen: '2026-01-08T00:00:00', lastSeen: '2026-02-01T00:00:00' }),
      ]),
    ).toEqual({ start: '2026-01-08', end: '2026-03-01' })
  })

  test('no detections means no window rather than a made-up one', () => {
    expect(detectionWindow([])).toBeNull()
  })

  test('day arithmetic ignores the time part, like the Python side', () => {
    expect(daysBetween('2026-02-01', '2026-02-23T12:00:00')).toBe(22)
  })
})
