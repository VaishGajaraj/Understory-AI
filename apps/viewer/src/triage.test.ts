function square(lon: number, lat: number, halfDeg = 0.002): Detection['geometry'] {
  return {
    type: 'Polygon',
    coordinates: [
      [
        [lon - halfDeg, lat - halfDeg],
        [lon + halfDeg, lat - halfDeg],
        [lon + halfDeg, lat + halfDeg],
        [lon - halfDeg, lat + halfDeg],
        [lon - halfDeg, lat - halfDeg],
      ],
    ],
  }
}

function detection(over: Partial<Detection> & Pick<Detection, 'id'>): Detection {
  return {
    geometry: square(-55, -7),
    firstSeen: '2026-02-13T00:00:00',
    lastSeen: '2026-03-01T00:00:00',
    score: 0.5,
    persistencePasses: 2,
    areaHa: 4,
    ...over,
  }
}

function label(over: Partial<DisturbanceEvent> & Pick<DisturbanceEvent, 'id'>): DisturbanceEvent {
  return {
    geometry: square(-55, -7),
    dateWindow: { start: '2026-02-01', end: '2026-02-13' },
    eventClass: 'access-road',
    status: 'confirmed',
    biome: 'toy',
    evidenceSource: 'synthetic fixture — generated, not observed',
    locationPrecision: 'exact',
    ...over,
  }
}

